package org.childrenscolorado.brocade;

public class ResourceGroup
{
	
	private String name = ""; // "key": "FITZ_DCX1SAN-fabric"
	private String type = ""; // "name": "FITZ_DCX1SAN-fabric"
	private String key = "";  // "type": "FC_FABRIC"
	private String bna_server = "";	// linkage between bna_server and the resource_group
	
	//////////////////////////////////////////////////////////////////////
	
	public ResourceGroup()
	{
		
	}
	
	//////////////////////////////////////////////////////////////////////
	
	public String getName()
	{
		return this.name;
	}
	
	//////////////////////////////////////////////////////////////////////
	
	public String getType()
	{
		return this.type;
	}
	
	//////////////////////////////////////////////////////////////////////
	
	public String getKey()
	{
		return this.key;
	}

	//////////////////////////////////////////////////////////////////////
	
	public String getBna_server()
	{
		return this.bna_server;
	}

	//////////////////////////////////////////////////////////////////////
	
	public void setBna_server(String bna_server)
	{
		this.bna_server = bna_server;
	}
	
	//////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////

}
